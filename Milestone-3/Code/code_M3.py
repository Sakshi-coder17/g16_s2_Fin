def build_dataset(raw):
    """
    Build modelling dataset from raw OHLCV data.
    Matches Das (2025) Section 5 preprocessing steps exactly.
    """
    def process(df):
        c   = df["Close"].values.flatten()
        ret = np.diff(np.log(c))
        vol = ewma_vol(ret)
        H   = df["High"].values.flatten()[1:]
        L   = df["Low"].values.flatten()[1:]
        C   = df["Close"].values.flatten()[1:]
        O   = df["Open"].values.flatten()[1:]
        gk  = garman_klass_vol(H, L, C, O)
        return pd.DataFrame({"ret": ret, "ewma": vol, "gk": gk},
                             index=df.index[1:])

    ndf = process(raw["NIFTY"])
    sdf = process(raw["SP500"]).rename(columns={"ret":"s_ret","ewma":"sp500_vol","gk":"sp500_gk"})
    gdf = process(raw["GOLD"]).rename(columns={"ret":"g_ret","ewma":"gold_vol","gk":"gold_gk"})

    df = (ndf.join(sdf[["sp500_vol"]], how="left")
             .join(gdf[["gold_vol"]], how="left")
             .rename(columns={"ewma":"nifty_vol"})
             .dropna())

    # Filter: only days where Nifty loss > MU% (tail events)
    tail = df[df["ret"] < -(MU/100)].copy()
    tail["y"] = np.abs(tail["ret"]) * 100     # y in percentage points

    n_total = len(df)
    n_tail  = len(tail)
    n_crash = (tail["y"] > Y0).sum()
    print(f"\n  Total trading days        : {n_total}")
    print(f"  Tail events  (loss > {MU}%) : {n_tail}  ({100*n_tail/n_total:.1f}%)")
    print(f"  Crash events (loss > {Y0}%) : {n_crash}  ({100*n_crash/n_tail:.1f}% of tail)")
    return tail.reset_index(drop=True)


def synthetic_data(n=350):
    """
    Calibrated synthetic fallback when yfinance is unavailable.
    Matches Das (2025) empirical statistics:
      ~4.6% days have loss>2%, ~11.6% of those exceed 5%.
    True params: xi=0.20, beta=[0.7, 0.4, 0.2, 0.15]
    """
    print("  [INFO] Using synthetic data calibrated to Das (2025) statistics.")
    nv = np.clip(np.random.gamma(4.0, 8.0, n), 8, 90)
    sv = np.clip(np.random.gamma(3.5, 6.0, n), 6, 55)
    gv = np.clip(np.random.gamma(2.5, 5.5, n), 4, 50)

    sc  = StandardScaler()
    cov = sc.fit_transform(np.column_stack([nv, sv, gv]))
    Xd  = np.column_stack([np.ones(n), cov])
    bt  = np.array([0.7, 0.4, 0.2, 0.15])
    sig = np.exp(Xd @ bt)
    xi  = 0.20
    u   = np.random.uniform(size=n)
    y   = np.clip(MU + sig/xi*(u**(-xi)-1), MU+1e-6, 30)

    df = pd.DataFrame({"y":y, "nifty_vol":nv, "sp500_vol":sv, "gold_vol":gv})
    print(f"\n  Synthetic tail events     : {n}")
    print(f"  Loss > {Y0}%  fraction     : {(df.y>Y0).mean()*100:.1f}%")
    return df, sc


def prepare():
    """Main data entry point — real data first, synthetic fallback."""
    raw = download_data()
    sc  = None

    if raw is None:
        df, sc = synthetic_data()
    else:
        df = build_dataset(raw)
        if len(df) < 50:
            df, sc = synthetic_data()

    y   = df["y"].values
    cov = df[["nifty_vol","sp500_vol","gold_vol"]].values

    if sc is None:
        sc  = StandardScaler()
        std = sc.fit_transform(cov)
    else:
        std = sc.transform(cov)

    X = np.column_stack([np.ones(len(y)), std])
    return y, X, cov, sc


# =============================================================================
# SECTION 2 — GPD LOG-LIKELIHOOD
# Das (2025) Section 3.1 — exact formula
# =============================================================================

def neg_ll(params, y, X):
    """
    Negative GPD regression log-likelihood (minimise this for MLE).

    Formula (Das 2025, Section 3.1):
      log L(xi, beta) = -sum_i [x_i^T beta]
                      + (1/xi - 1) * sum_i log(1 + xi*(y_i-mu)/exp(x_i^T beta))

    Special case xi→0: reduces to exponential with mean exp(x^T beta).
    Returns +inf for invalid regions (where GPD support is violated).
    """
    xi    = params[0]
    beta  = params[1:]
    lsig  = X @ beta                         # log sigma_i  (n,)
    sigma = np.exp(lsig)
    z     = 1.0 + xi * (y - MU) / sigma     # must be > 0

    if np.any(z <= 0):
        return np.inf

    if abs(xi) < 1e-8:                       # exponential limit
        ll = -np.sum(lsig) - np.sum((y - MU) / sigma)
    else:
        ll = -np.sum(lsig) + (1.0/xi - 1.0) * np.sum(np.log(z))

    return -ll   # negate: scipy minimises, we want to MAXIMISE log-likelihood


# =============================================================================
# SECTION 3 — MLE FITTING
# Key M3 change: no prior term. Pure likelihood maximisation.
# =============================================================================

def fit_mle(y, X):
    """
    Fit GPD Regression via Maximum Likelihood Estimation.

    M2 vs M3 difference:
      M2 minimised: -(log_likelihood + log_prior)   [Bayesian MAP, Cauchy prior]
      M3 minimises: -(log_likelihood)               [Pure MLE, no prior]

    Initialisation (method-of-moments):
      xi_0      = 0.10
      beta_0[0] = log(mean(y-mu) * (1-xi_0))   intercept via MOM
      beta_0[j] = 0  for j > 0

    Constraint: xi < 1 (Smith 1985, cited in Das 2025 Section 3.1)

    Standard errors: from observed Fisher information matrix
      H = numerical Hessian of NLL at MLE
      Cov = H^{-1}
      SE  = sqrt(diag(Cov))
    """
    p   = X.shape[1]
    xi0 = 0.10
    b0  = np.zeros(p)
    b0[0] = np.log(max(np.mean(y - MU) * (1 - xi0), 1e-4))
    x0  = np.concatenate([[xi0], b0])

    result = minimize(neg_ll, x0, args=(y, X),
                      method="L-BFGS-B",
                      bounds=[(-3.0, 0.95)] + [(None,None)]*p,
                      options={"maxiter":10000, "ftol":1e-12, "gtol":1e-7})

    if not result.success or not np.isfinite(result.fun):
        # Gradient-free fallback
        result = minimize(neg_ll, x0, args=(y, X),
                          method="Nelder-Mead",
                          options={"maxiter":100000, "xatol":1e-9, "fatol":1e-9})

    xi_hat   = float(result.x[0])
    beta_hat = result.x[1:]

    # Observed Fisher information (numerical second derivatives)
    h    = 1e-4
    np_  = len(result.x)
    H    = np.zeros((np_, np_))
    for i in range(np_):
        for j in range(i, np_):
            ei = np.zeros(np_); ej = np.zeros(np_)
            ei[i] = h;          ej[j] = h
            fpp = neg_ll(result.x+ei+ej, y, X)
            fpm = neg_ll(result.x+ei-ej, y, X)
            fmp = neg_ll(result.x-ei+ej, y, X)
            fmm = neg_ll(result.x-ei-ej, y, X)
            H[i,j] = H[j,i] = (fpp - fpm - fmp + fmm) / (4*h**2)
    H = 0.5*(H + H.T)
    try:    cov_mat = np.linalg.inv(H)
    except: cov_mat = np.linalg.pinv(H)
    se = np.sqrt(np.abs(np.diag(cov_mat)))

    return result, xi_hat, beta_hat, se


