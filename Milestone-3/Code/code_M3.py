def print_results(xi_hat, beta_hat, se, cov_names, n, aic_v, bic_v,
                  cp10, cp90, e10, e90):
    """
    Print MLE results table to console.
    No train/test split — MLE is a deterministic statistical estimator
    that uses ALL data to find the unique (xi_hat, beta_hat) maximising
    the log-likelihood.  No prediction on held-out data is needed.
    """
    z95    = norm.ppf(0.975)
    pnames = ["xi  (shape)", "b0  Intercept"] + cov_names
    ests   = np.concatenate([[xi_hat], beta_hat])
    sep    = "=" * 72

    print(f"\n{sep}")
    print("  CSE 400  |  Milestone 3 -- GPD REGRESSION: MLE RESULTS")
    print("  Group: s2_g16_fin  |  SEAS, Ahmedabad University")
    print(sep)
    print(f"  Method      : Maximum Likelihood Estimation  (MLE)")
    print(f"  M2 method   : Bayesian MAP with Cauchy prior  [REPLACED by MLE]")
    print(f"  Model type  : DETERMINISTIC parametric model")
    print(f"                y_i ~ GPD(mu, sigma_i, xi)")
    print(f"                log(sigma_i) = x_i^T * beta")
    print(f"                MLE gives unique fixed point estimate.")
    print(f"                No prior, no posterior, no train/test split.")
    print(f"  Threshold   : mu = {MU}%  |  Crash threshold: y0 = {Y0}%")
    print(f"  Observations: n = {n}  (all tail events, loss > {MU}%)")
    print(f"  Covariates  : Nifty vol, S&P 500 vol, Gold vol  [standardised]\n")

    print(f"  {'Parameter':<24} {'Estimate':>9} {'Std Err':>9} "
          f"{'95% Lo':>9} {'95% Hi':>9}  Sig")
    print("  " + "-" * 68)
    for nm, est, sv in zip(pnames, ests, se):
        lo  = est - z95 * sv
        hi  = est + z95 * sv
        sig = "***" if (lo > 0 or hi < 0) else ""
        print(f"  {nm:<24} {est:>9.4f} {sv:>9.4f} {lo:>9.4f} {hi:>9.4f}  {sig}")
    print("  (*** = 95% CI excludes zero)\n")

    print(f"  {'---'*23}")
    print(f"  MODEL FIT  (cf. Das 2025 Table 2)")
    print(f"  AIC  : {aic_v:.2f}   (lower = better fit, penalises parameters)")
    print(f"  BIC  : {bic_v:.2f}   (lower = better fit, stronger penalty)\n")

    print(f"  ECONOMIC INTERPRETATION  (cf. Das 2025 Section 5)")
