def _gpd_pdf(y_arr, mu, sigma, xi):
    """
    GPD probability density function — Das (2025), Section 3.

        f(y) = (1/sigma) * [1 + xi*(y-mu)/sigma]^{-1/xi - 1}

    Vectorised over y_arr; returns 0 outside support.
    xi = 0 limit uses the exponential density.
    """
    if abs(xi) < 1e-8:
        return (1.0 / sigma) * np.exp(-(y_arr - mu) / sigma)
    z = 1.0 + xi * (y_arr - mu) / sigma
    return np.where(
        z > 0,
        (1.0 / sigma) * np.power(np.clip(z, 1e-14, None), -(1.0 / xi + 1.0)),
        0.0
    )


def _gpd_cdf(y_arr, mu, sigma, xi):
    """
    GPD cumulative distribution function — Das (2025), Section 3.

        F(y) = 1 - [1 + xi*(y-mu)/sigma]^{-1/xi}

    xi = 0 limit uses the exponential CDF.
    """
    if abs(xi) < 1e-8:
        return np.clip(1.0 - np.exp(-(y_arr - mu) / sigma), 0.0, 1.0)
    z = 1.0 + xi * (y_arr - mu) / sigma
    sf = np.where(z > 0, np.power(np.clip(z, 1e-14, None), -1.0 / xi), 0.0)
    return np.clip(1.0 - sf, 0.0, 1.0)


def _gpd_quantile(p, mu, sigma, xi):
    """
    GPD quantile function Q(p) = F^{-1}(p) — Das (2025), Section 3.

        Q(p) = mu + sigma/xi * [(1-p)^{-xi} - 1],   xi != 0
        Q(p) = mu - sigma * ln(1-p),                 xi =  0
    """
    p = np.clip(p, 1e-9, 1.0 - 1e-9)
    if abs(xi) < 1e-8:
        return mu - sigma * np.log(1.0 - p)
    return mu + sigma / xi * (np.power(1.0 - p, -xi) - 1.0)


def _scenario_sigma(v_std, beta_hat, med_oth):
    """Return sigma_i = exp(x^T beta) for a given standardised Nifty vol value."""
    row = np.concatenate([[1.0, v_std], med_oth]).reshape(1, -1)
    return float(np.exp(np.dot(row, beta_hat)).ravel()[0])


def _vol_scenarios(X, beta_hat, scaler):
    """
    Compute sigma_i at 10th / 50th / 90th percentile of Nifty vol.
    Returns (v_pct, sigmas) where v_pct is the original-scale vol in %.
    """
    v10 = np.percentile(X[:, 1], 10)
    v50 = np.percentile(X[:, 1], 50)
    v90 = np.percentile(X[:, 1], 90)
    med_oth = np.median(X[:, 2:], axis=0)

    sig_lo  = _scenario_sigma(v10, beta_hat, med_oth)
    sig_med = _scenario_sigma(v50, beta_hat, med_oth)
    sig_hi  = _scenario_sigma(v90, beta_hat, med_oth)

    d_sc       = np.zeros((3, X.shape[1] - 1))
    d_sc[:, 0] = [v10, v50, v90]
    v_pct      = scaler.inverse_transform(d_sc)[:, 0]

    return v_pct, sig_lo, sig_med, sig_hi


