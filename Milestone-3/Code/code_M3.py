# SECTION 5 — SIMULATION STUDY
# Replicates Das (2025) Section 4 with MLE instead of MAP
# =============================================================================

def run_simulation(N=100, n=100, p=5):
    """
    Simulation study — Das (2025) Section 4 setup, M3 uses MLE.

    For each of N datasets:
      1. Draw  X ~ N(0,I),  beta ~ N(0,1),  xi ~ Uniform(-0.5, 0.5)
      2. Generate y ~ GPD(mu=2, exp(X*beta), xi)
      3. Fit MLE on 80% train
      4. Evaluate on 20% test

    Metrics (aggregated by median, as in paper):
      RMSE(y)    prediction error on test set
      RMSE(beta) coefficient estimation error
      RMSE(xi)   shape estimation error
      AIC, BIC   in-sample fit (NLL based, no prior)
    """
    rmse_y, rmse_b, rmse_x, aics, bics = [], [], [], [], []
    xi_estimates, xi_trues = [], []

    print(f"\n{'='*60}")
    print(f"  SIMULATION STUDY  (N={N}, n={n}, p={p})")
    print(f"  M3 method: MLE  |  M2 used: Bayesian MAP (Cauchy prior)")
    print(f"  Paper setup: Das (2025) Section 4")
    print(f"{'='*60}")

    for s in range(N):
        Xd   = np.column_stack([np.ones(n), np.random.randn(n, p)])
        bt   = np.random.randn(p+1)
        xit  = np.random.uniform(-0.5, 0.5)
        sig  = np.exp(Xd @ bt)
        u    = np.random.uniform(size=n)
        ysim = (MU - sig*np.log(u) if abs(xit)<1e-8
                else np.clip(MU + sig/xit*(u**(-xit)-1), MU+1e-6, 500))

        idx  = int(0.8*n)
        try:
            res, xm, bm, _ = fit_mle(ysim[:idx], Xd[:idx])
            if not np.isfinite(res.fun): continue
            yp  = cond_mean(xm, bm, Xd[idx:])
            fin = np.isfinite(yp)
            if fin.sum() < 5: continue
            av, bv = aic_bic(res.fun, idx, len(res.x))
            rmse_y.append(np.sqrt(np.mean((yp[fin]-ysim[idx:][fin])**2)))
            rmse_b.append(np.sqrt(np.mean((bm-bt)**2)))
            rmse_x.append(abs(xm-xit))
            aics.append(av); bics.append(bv)
            xi_estimates.append(xm); xi_trues.append(xit)
        except: continue

    summary = pd.DataFrame({
        "Metric"  : ["RMSE (y)", "RMSE (β)", "RMSE (ξ)", "AIC", "BIC"],
        "MLE (M3)": [np.nanmedian(rmse_y), np.nanmedian(rmse_b),
                     np.nanmedian(rmse_x), np.nanmedian(aics), np.nanmedian(bics)],
        "Das (2025) MAP-Cauchy": [11.36, 0.10, 0.08, 162.67, 176.97],
    })

    print("\n" + summary.to_string(index=False))
    print("\n  Note: MAP-Cauchy values from Das (2025) Table 1.")
    print(f"  Successful fits: {len(rmse_y)} / {N}")

    return summary, np.array(rmse_y), np.array(rmse_b), np.array(rmse_x), \
           np.array(xi_estimates), np.array(xi_trues)


