def ewma_vol(returns, k=K_WIN, alpha=ALPHA):
    """
    EWMA empirical volatility — Das (2025) Section 2, Equation 1:
        sigma^2_t = alpha * s^2_{t-1}  +  (1-alpha) * r^2_t
    Annualised: multiply by sqrt(250).
    """
    n   = len(returns)
    vol = np.full(n, np.nan)
    for t in range(k, n):
        s2   = np.var(returns[t - k + 1 : t], ddof=1)
        vol[t] = np.sqrt(alpha * s2 + (1 - alpha) * returns[t]**2) * np.sqrt(250)
    return vol


def garman_klass_vol(H, L, C, O):
    """
    Garman-Klass intraday volatility — Das (2025) Section 2, Equation 2:
        sigma^2_GK = 0.5*(log H/L)^2 - (2*ln2-1)*(log C/O)^2
    Annualised: multiply by sqrt(250).
    """
    gk = 0.5*(np.log(H/L))**2 - (2*np.log(2)-1)*(np.log(C/O))**2
    return np.sqrt(np.abs(gk)) * np.sqrt(250)


