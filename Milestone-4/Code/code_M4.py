def run_simulation_m4(N=100, n=100, p=5,
                      lam_lasso=1.0, lam_ridge=5.0, g_val=3.0):
    """Monte Carlo simulation comparing MLE vs all 4 Bayesian MAP priors.

    Das (2025) Section 4 setup — N=100 datasets, n=100 obs, p=5 covariates.
    Each run:
      1. Draw X ~ N(0,I), beta ~ N(0,1), xi ~ Uniform(-0.5, 0.5)
      2. Generate y ~ GPD(mu=2, exp(X*beta), xi)
      3. Fit ALL 5 methods on the FULL dataset (no train/test split)
      4. Evaluate RMSE(y), AIC, BIC on full data
      5. Record RMSE(beta) and RMSE(xi) vs true parameter values

    Returns summary DataFrame and records dict.
    """
    methods = ["MLE (M3)", "Cauchy", "Lasso", "Ridge", "g-prior"]
    rec = {m: {"rmse_y": [], "rmse_b": [], "rmse_x": [], "aic": [], "bic": []}
           for m in methods}

    print(f"\n{'=' * 65}")
    print(f"  M4 SIMULATION  N={N}, n={n}, p={p}")
    print(f"  Full dataset fit — no train/test split (statistical model).")
    print(f"  Metrics: RMSE(y), AIC, BIC, RMSE(beta), RMSE(xi)")
    print(f"  Lasso lam={lam_lasso:.3f} | Ridge lam={lam_ridge:.3f} | g={g_val:.3f}")
    print(f"{'=' * 65}")

    for _ in range(N):
        Xd   = np.column_stack([np.ones(n), np.random.randn(n, p)])
        bt   = np.random.randn(p + 1)
        xit  = np.random.uniform(-0.5, 0.5)
        sig  = np.exp(Xd @ bt)
        u    = np.random.uniform(size=n)
        ysim = (MU - sig * np.log(u) if abs(xit) < 1e-8
                else np.clip(MU + sig / xit * (u**(-xit) - 1),
                             MU + 1e-6, 500))

        # Fit on FULL data — this is a statistical model, not ML
        fits = {}
        try:
            r, xm, bm, _ = fit_mle(ysim, Xd)
            if np.isfinite(r.fun):
                fits["MLE (M3)"] = (r.fun, xm, bm)
        except Exception:
            pass
        for name, fn, extra in [
            ("Cauchy",  fit_cauchy,  ()),
            ("Lasso",   fit_lasso,   (lam_lasso,)),
            ("Ridge",   fit_ridge,   (lam_ridge,)),
            ("g-prior", fit_gprior,  (g_val,)),
        ]:
            try:
                r2, xm, bm = fn(ysim, Xd, *extra)
                nll = neg_ll(np.concatenate([[xm], bm]), ysim, Xd)
                if np.isfinite(nll):
                    fits[name] = (nll, xm, bm)
            except Exception:
                pass

        for m, (nll_v, xm, bm) in fits.items():
            try:
                yp  = cond_mean(xm, bm, Xd)
                fin = np.isfinite(yp)
                if fin.sum() < 3:
                    continue
                av, bv = aic_bic(nll_v, n=n, k=len(bm) + 1)
                rec[m]["rmse_y"].append(
                    np.sqrt(np.mean((yp[fin] - ysim[fin])**2)))
                rec[m]["rmse_b"].append(np.sqrt(np.mean((bm - bt)**2)))
                rec[m]["rmse_x"].append(abs(xm - xit))
                rec[m]["aic"].append(av)
                rec[m]["bic"].append(bv)
            except Exception:
                pass

    rows = []
    for m in methods:
        d = rec[m]
        rows.append({
            "Method":   m,
            "RMSE(y)":  np.nanmedian(d["rmse_y"]) if d["rmse_y"] else np.nan,
            "RMSE(B)":  np.nanmedian(d["rmse_b"]) if d["rmse_b"] else np.nan,
            "RMSE(xi)": np.nanmedian(d["rmse_x"]) if d["rmse_x"] else np.nan,
            "AIC":      np.nanmedian(d["aic"])     if d["aic"]    else np.nan,
            "BIC":      np.nanmedian(d["bic"])     if d["bic"]    else np.nan,
            "N_ok":     len(d["rmse_y"]),
        })
    summary = pd.DataFrame(rows)
    print("\n" + summary.to_string(index=False))
    return summary, rec


