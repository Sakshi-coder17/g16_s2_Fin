def _init_params(y, X):
    """Warm-start initialisation shared by all fitters."""
    p   = X.shape[1]
    xi0 = 0.10
    b0  = np.zeros(p)
    b0[0] = np.log(max(np.mean(y - MU) * (1 - xi0), 1e-4))
    return np.concatenate([[xi0], b0])


def _optimise(obj, x0, args, p):
    """L-BFGS-B with Nelder-Mead fallback."""
    bounds = [(-3.0, 0.95)] + [(None, None)] * p
    res = minimize(obj, x0, args=args, method="L-BFGS-B", bounds=bounds,
                   options={"maxiter": 10000, "ftol": 1e-12, "gtol": 1e-7})
    if not res.success or not np.isfinite(res.fun):
        res = minimize(obj, x0, args=args, method="Nelder-Mead",
                       options={"maxiter": 100000, "xatol": 1e-9, "fatol": 1e-9})
    return res


def fit_cauchy(y, X):
    x0 = _init_params(y, X)
    res = _optimise(nlp_cauchy, x0, (y, X), X.shape[1])
    return res, float(res.x[0]), res.x[1:]


def fit_lasso(y, X, lam):
    x0 = _init_params(y, X)
    res = _optimise(nlp_lasso, x0, (y, X, lam), X.shape[1])
    return res, float(res.x[0]), res.x[1:]


def fit_ridge(y, X, lam):
    x0 = _init_params(y, X)
    res = _optimise(nlp_ridge, x0, (y, X, lam), X.shape[1])
    return res, float(res.x[0]), res.x[1:]


def fit_gprior(y, X, g):
    x0 = _init_params(y, X)
    res = _optimise(nlp_gprior, x0, (y, X, g), X.shape[1])
    return res, float(res.x[0]), res.x[1:]


