def _gpd_pdf(y_arr, mu, sigma, xi):
    """GPD probability density function - Das (2025) Section 3."""
    if abs(xi) < 1e-8:
        return (1.0 / sigma) * np.exp(-(y_arr - mu) / sigma)
    z = 1.0 + xi * (y_arr - mu) / sigma
    return np.where(
        z > 0,
        (1.0 / sigma) * np.power(np.clip(z, 1e-14, None), -(1.0 / xi + 1.0)),
        0.0)


def _gpd_cdf(y_arr, mu, sigma, xi):
    """GPD cumulative distribution function - Das (2025) Section 3."""
    if abs(xi) < 1e-8:
        return np.clip(1.0 - np.exp(-(y_arr - mu) / sigma), 0.0, 1.0)
    z  = 1.0 + xi * (y_arr - mu) / sigma
    sf = np.where(z > 0, np.power(np.clip(z, 1e-14, None), -1.0 / xi), 0.0)
    return np.clip(1.0 - sf, 0.0, 1.0)


