def fit_mle(y, X):
    """Fit GPD Regression via MLE (M3 deterministic baseline).
    Returns (result, xi_hat, beta_hat, standard_errors)."""
    p   = X.shape[1]
    xi0 = 0.10
    b0  = np.zeros(p)
    b0[0] = np.log(max(np.mean(y - MU) * (1 - xi0), 1e-4))
    x0  = np.concatenate([[xi0], b0])

    res = minimize(neg_ll, x0, args=(y, X),
                   method="L-BFGS-B",
                   bounds=[(-3.0, 0.95)] + [(None, None)] * p,
                   options={"maxiter": 10000, "ftol": 1e-12, "gtol": 1e-7})

    if not res.success or not np.isfinite(res.fun):
        res = minimize(neg_ll, x0, args=(y, X),
                       method="Nelder-Mead",
                       options={"maxiter": 100000, "xatol": 1e-9, "fatol": 1e-9})

    xi_hat   = float(res.x[0])
    beta_hat = res.x[1:]

    # Standard errors from numerical Hessian (observed Fisher information)
    h   = 1e-4
    np_ = len(res.x)
    H   = np.zeros((np_, np_))
    for i in range(np_):
        for j in range(i, np_):
            ei = np.zeros(np_); ej = np.zeros(np_)
            ei[i] = h;          ej[j] = h
            fpp = neg_ll(res.x + ei + ej, y, X)
            fpm = neg_ll(res.x + ei - ej, y, X)
            fmp = neg_ll(res.x - ei + ej, y, X)
            fmm = neg_ll(res.x - ei - ej, y, X)
            H[i, j] = H[j, i] = (fpp - fpm - fmp + fmm) / (4 * h**2)
    H = 0.5 * (H + H.T)
    try:
        cov_mat = np.linalg.inv(H)
    except np.linalg.LinAlgError:
        cov_mat = np.linalg.pinv(H)
    se = np.sqrt(np.abs(np.diag(cov_mat)))

    return res, xi_hat, beta_hat, se


