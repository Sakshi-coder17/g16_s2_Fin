def cond_mean(xi, beta, X):
    """E[Y | Y >= mu, x] = mu + sigma / (1 - xi),  xi < 1."""
    if xi >= 1.0:
        return np.full(len(X), np.inf)
    return MU + np.exp(X @ beta) / (1.0 - xi)


def crash_prob(xi, beta, X, y0=Y0):
    """P(Y > y0 | Y > mu, x) = (1 + xi*(y0-mu)/sigma)^{-1/xi}."""
    sigma = np.exp(X @ beta)
    z     = np.clip(1.0 + xi * (y0 - MU) / sigma, 1e-10, None)
    return z**(-1.0 / xi)


def aic_bic(nll_val, n, k):
    """AIC and BIC from negative log-likelihood value."""
    return 2 * k + 2 * nll_val, k * np.log(n) + 2 * nll_val


def eval_model(xi_hat, beta_hat, nll_val, y, X, n, p):
    """Compute in-sample RMSE, AIC and BIC using ALL observations.
    Classical statistics (GPD/EVT) uses full data - no ML train/test split.
    RMSE = in-sample conditional mean prediction error.
    AIC  = 2k - 2*log-likelihood  (penalises complexity).
    BIC  = k*log(n) - 2*log-likelihood  (stricter penalty)."""
    y_pred       = cond_mean(xi_hat, beta_hat, X)
    fin          = np.isfinite(y_pred)
    rmse         = (np.sqrt(np.mean((y_pred[fin] - y[fin])**2))
                    if fin.sum() > 0 else np.nan)
    aic_v, bic_v = aic_bic(nll_val, n=n, k=p + 1)
    return rmse, aic_v, bic_v


