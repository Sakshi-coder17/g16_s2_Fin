def nlp_cauchy(params, y, X):
    """Negative log-posterior: Cauchy prior on BOTH beta AND xi.

    Das (2025) Sections 3.2 and 3.3 specify two separate Cauchy priors:

    (a) Standard Cauchy on each beta_j  (Section 3.3):
            log p(beta_j) = -log(1 + beta_j^2)

    (b) Truncated Cauchy(0,1) on xi, truncated above at xi=1 (Section 3.2):
            p_trunc(xi) = 4 / (3*pi*(1 + xi^2))   for xi < 1
            log p(xi) = -log(1 + xi^2)  + const
        The normalisation constant cancels in MAP, so we only need:
            -log p(xi)  +=  log(1 + xi^2)

    Combined NLP = neg_ll + sum_j log(1+beta_j^2) + log(1+xi^2)
    """
    nll_val = neg_ll(params, y, X)
    if not np.isfinite(nll_val):
        return np.inf
    xi   = params[0]
    beta = params[1:]
    # Cauchy penalty on beta (Section 3.3)
    prior_beta = np.sum(np.log(1.0 + beta**2))
    # Truncated Cauchy penalty on xi (Section 3.2) — enforced by bound xi < 1
    prior_xi   = np.log(1.0 + xi**2)
    return nll_val + prior_beta + prior_xi


def nlp_lasso(params, y, X, lam):
    """Negative log-posterior with Lasso (Laplace) prior.
    log p(beta) = -lambda * sum_j |beta_j|
    Promotes sparsity (zero coefficients). Lambda via 5-fold CV."""
    nll_val = neg_ll(params, y, X)
    if not np.isfinite(nll_val):
        return np.inf
    beta = params[1:]
    return nll_val + lam * np.sum(np.abs(beta))


def nlp_ridge(params, y, X, lam):
    """Negative log-posterior with Ridge (Gaussian) prior.
    log p(beta) = -(lambda/2) * sum_j beta_j^2
    Uniform shrinkage; no sparsity. Lambda via 5-fold CV."""
    nll_val = neg_ll(params, y, X)
    if not np.isfinite(nll_val):
        return np.inf
    beta = params[1:]
    return nll_val + lam * np.sum(beta**2)


def nlp_gprior(params, y, X, g):
    """Negative log-posterior with Zellner's g-prior.
    log p(beta) = -(1/2g) * beta^T (X^T X) beta
    Encodes prior relative to data information. g via 5-fold CV."""
    nll_val = neg_ll(params, y, X)
    if not np.isfinite(nll_val):
        return np.inf
    beta = params[1:]
    XtX  = X.T @ X
    return nll_val + (1.0 / (2.0 * g)) * float(beta @ XtX @ beta)


