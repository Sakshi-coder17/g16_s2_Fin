def neg_ll(params, y, X):
    """Negative GPD regression log-likelihood (minimise for MLE).
    params = [xi, beta_0, ..., beta_p]
    Das (2025) Section 3.1, Eq. log L(xi, beta)."""
    xi    = params[0]
    beta  = params[1:]
    lsig  = X @ beta
    sigma = np.exp(lsig)
    z     = 1.0 + xi * (y - MU) / sigma

    if np.any(z <= 0):
        return np.inf

    if abs(xi) < 1e-8:
        ll = -np.sum(lsig) - np.sum((y - MU) / sigma)
    else:
        ll = -np.sum(lsig) + (1.0 / xi - 1.0) * np.sum(np.log(z))

    return -ll


