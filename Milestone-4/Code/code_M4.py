def cv_gprior(y, X, g_grid=None, k=5):
    if g_grid is None:
        g_grid = np.logspace(-2, 2, 50)
    print("  [g-prior] 5-fold CV for g ...")
    best, curve = _kfold_cv(y, X, fit_gprior, g_grid, k)
    return best, g_grid, curve


