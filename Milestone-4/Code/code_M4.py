def main():
    print("\n" + "=" * 72)
    print("  CSE 400: Fundamentals of Probability in Computing")
    print("  Group: s2_g16_fin  |  Milestone 4  |  Bayesian GPD Regression")
    print("  Reference: Das (2025) arXiv:2506.17549")
    print("=" * 72)

    # 1. Data
    y, X, cov_raw, scaler = prepare()
    n = len(y)
    p = X.shape[1]
    print(f"\n  M3 baseline design (p=4): [1, nifty_ewma, sp500_ewma, gold_ewma]")
    print(f"  M4 design matrix  (p=7): [1, nifty_ewma, sp500_ewma, gold_ewma,")
    print(f"                                nifty_gk,   sp500_gk,   gold_gk]")
    print(f"  (M4 adds 3 Garman-Klass covariates — Das 2025, Section 5)")
    print(f"  M4 X shape: {X.shape}  |  Tail observations: n = {n}")

    # ── ESTIMATION STRATEGY ───────────────────────────────────────────────────
    # M3 (MLE):  deterministic, frequentist, p=4 covariates.
    #            Single point estimate; no prior; maximum likelihood only.
    # M4 (MAP):  randomized (prior-based), Bayesian, p=7 covariates.
    #            Extends M3 by: (a) adding Garman-Klass covariates,
    #                           (b) incorporating prior distributions on beta
    #                               (and on xi for Cauchy — Das 2025 Sec 3.2),
    #                           (c) regularising via 4 different prior choices.
    # Both M3 and M4 fit on ALL available data (full likelihood, no hold-out).
    # Model comparison uses in-sample AIC and BIC (information criteria).
    # RMSE is also in-sample for consistency with the empirical study.
    # Note: The simulation study (Section 10/Plot 7) also fits on full
    #       synthetic data — no train/test split anywhere in this code.

    # 3. M3 MLE baseline (identical to Milestone 3 — no changes)
    print("\n[M3 Baseline] MLE (Milestone 3 method — kept unchanged) ...")
    print("  Fitting GPD Regression by Maximum Likelihood Estimation ...")
    res_mle, xi_mle, beta_mle, _ = fit_mle(y, X)
    nll_mle = neg_ll(np.concatenate([[xi_mle], beta_mle]), y, X)
    rmse_mle, aic_mle, bic_mle = eval_model(
        xi_mle, beta_mle, nll_mle, y, X, n, p)
    print(f"  xi={xi_mle:.4f}  RMSE={rmse_mle:.4f}  "
          f"AIC={aic_mle:.2f}  BIC={bic_mle:.2f}")
    print("  --> M3 (MLE) gives the DETERMINISTIC baseline. M4 extends this")
    print("      by replacing the point estimate with PRIOR-REGULARISED MAP.")

    # 4. M4 Cauchy  (Bayesian MAP — Cauchy prior on beta AND xi)
    print("\n[M4 - Step 1] Cauchy prior MAP ...")
    print("  Cauchy prior on beta (heavy-tailed regularisation)")
    print("  + Truncated Cauchy prior on xi (Das 2025 Section 3.2)")
    _, xi_cau, beta_cau = fit_cauchy(y, X)
    nll_cau = neg_ll(np.concatenate([[xi_cau], beta_cau]), y, X)
    rmse_cau, aic_cau, bic_cau = eval_model(
        xi_cau, beta_cau, nll_cau, y, X, n, p)
    print(f"  xi={xi_cau:.4f}  RMSE={rmse_cau:.4f}  "
          f"AIC={aic_cau:.2f}  BIC={bic_cau:.2f}")

    # 5. M4 Lasso  (L1 prior — promotes sparsity)
    print("\n[M4 - Step 2] Lasso prior MAP (5-fold CV for lambda) ...")
    print("  Lasso (Laplace) prior encourages sparse coefficients.")
    print("  Lambda selected by minimising 5-fold cross-validation RMSE.")
    best_lam_l, lam_grid_l, cv_l = cv_lasso(y, X)
    _, xi_las, beta_las = fit_lasso(y, X, best_lam_l)
    nll_las = neg_ll(np.concatenate([[xi_las], beta_las]), y, X)
    rmse_las, aic_las, bic_las = eval_model(
        xi_las, beta_las, nll_las, y, X, n, p)
    print(f"  xi={xi_las:.4f}  RMSE={rmse_las:.4f}  "
          f"AIC={aic_las:.2f}  BIC={bic_las:.2f}")

    # 6. M4 Ridge  (L2 prior — uniform shrinkage)
    print("\n[M4 - Step 3] Ridge prior MAP (5-fold CV for lambda) ...")
    print("  Ridge (Gaussian) prior shrinks all coefficients uniformly.")
    print("  Lambda selected by minimising 5-fold cross-validation RMSE.")
    best_lam_r, lam_grid_r, cv_r = cv_ridge(y, X)
    _, xi_rid, beta_rid = fit_ridge(y, X, best_lam_r)
    nll_rid = neg_ll(np.concatenate([[xi_rid], beta_rid]), y, X)
    rmse_rid, aic_rid, bic_rid = eval_model(
        xi_rid, beta_rid, nll_rid, y, X, n, p)
    print(f"  xi={xi_rid:.4f}  RMSE={rmse_rid:.4f}  "
          f"AIC={aic_rid:.2f}  BIC={bic_rid:.2f}")

    # 7. M4 g-prior  (Zellner's g-prior — data-informed shrinkage)
    print("\n[M4 - Step 4] Zellner g-prior MAP (5-fold CV for g) ...")
    print("  g-prior: beta ~ N(0, g*(X'X)^-1) — shrinkage relative to data.")
    print("  g selected by minimising 5-fold cross-validation RMSE.")
    best_g, g_grid, cv_g = cv_gprior(y, X)
    _, xi_gpr, beta_gpr = fit_gprior(y, X, best_g)
    nll_gpr = neg_ll(np.concatenate([[xi_gpr], beta_gpr]), y, X)
    rmse_gpr, aic_gpr, bic_gpr = eval_model(
        xi_gpr, beta_gpr, nll_gpr, y, X, n, p)
    print(f"  xi={xi_gpr:.4f}  RMSE={rmse_gpr:.4f}  "
          f"AIC={aic_gpr:.2f}  BIC={bic_gpr:.2f}")

    # 8. Consolidate
    fits_dict = {
        "MLE (M3)": {"xi": xi_mle,  "beta": beta_mle,  "X_all": X},
        "Cauchy":   {"xi": xi_cau,  "beta": beta_cau,  "X_all": X},
        "Lasso":    {"xi": xi_las,  "beta": beta_las,  "X_all": X},
        "Ridge":    {"xi": xi_rid,  "beta": beta_rid,  "X_all": X},
        "g-prior":  {"xi": xi_gpr,  "beta": beta_gpr,  "X_all": X},
    }
    eval_df = pd.DataFrame({
        "Method": ["MLE (M3)", "Cauchy", "Lasso", "Ridge", "g-prior"],
        "RMSE":   [rmse_mle, rmse_cau, rmse_las, rmse_rid, rmse_gpr],
        "AIC":    [aic_mle,  aic_cau,  aic_las,  aic_rid,  aic_gpr],
        "BIC":    [bic_mle,  bic_cau,  bic_las,  bic_rid,  bic_gpr],
    })
    best_prior = eval_df.loc[eval_df["RMSE"].idxmin(), "Method"]

    # 9. Results table
    print_results_m4(fits_dict, eval_df, n, best_prior)

    # 10. Plots
    print("\n-- Plot 1: Diagnostics --")
    plot_diagnostics_m4(y, fits_dict, cov_raw)

    print("-- Plot 2: Coefficients --")
    plot_coefficients_m4(fits_dict)

    print("-- Plot 3: CV Curves --")
    plot_cv_curves(
        (lam_grid_l, cv_l, best_lam_l),
        (lam_grid_r, cv_r, best_lam_r),
        (g_grid,     cv_g, best_g),
    )

    print("-- Plot 4: Crash Probability Curves --")
    plot_crash_curves_m4(fits_dict, X, scaler)

    print("-- Plot 5: GPD PDF --")
    plot_pdf_m4(fits_dict, y, X)

    print("-- Plot 6: GPD CDF --")
    plot_cdf_m4(fits_dict, y, X)

    print("-- Plot 7: Simulation Study --")
    sim_summary, records = run_simulation_m4(
        N=100, n=100, p=5,
        lam_lasso=best_lam_l,
        lam_ridge=best_lam_r,
        g_val=best_g)
    plot_simulation_m4(sim_summary, records)

    print("-- Plot 8: Master Comparison M3 vs M4 --")
    plot_metrics_comparison(eval_df)

    print(f"\n  Milestone 4 complete.  Best prior: {best_prior}\n")


if __name__ == "__main__":
    main()