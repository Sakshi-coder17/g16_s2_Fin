    y   = np.clip(MU + sig / xi * (u**(-xi) - 1), MU + 1e-6, 30)

    df = pd.DataFrame({"y": y, "nifty_ewma": nv, "sp500_ewma": sv,
                       "gold_ewma": gv, "nifty_gk": gk_n,
                       "sp500_gk": gk_s, "gold_gk": gk_g})
    print(f"  Synthetic tail obs: {n}  |  "
          f"Loss > {Y0}%: {(df.y > Y0).mean() * 100:.1f}%")
    return df, sc


