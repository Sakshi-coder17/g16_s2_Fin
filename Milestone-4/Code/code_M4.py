    sv   = np.clip(np.random.gamma(3.5, 6.0, n), 6, 55)
    gv   = np.clip(np.random.gamma(2.5, 5.5, n), 4, 50)
    gk_n = nv * np.random.uniform(0.6, 1.4, n)
    gk_s = sv * np.random.uniform(0.6, 1.4, n)
    gk_g = gv * np.random.uniform(0.6, 1.4, n)

