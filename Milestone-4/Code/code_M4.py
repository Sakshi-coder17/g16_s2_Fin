def _kfold_cv(y, X, fit_fn, param_grid, k=5):
    """Generic k-fold CV over a 1-D regularisation parameter grid.
    Returns (best_param, cv_rmse_array)."""
    n        = len(y)
    fold_idx = np.array_split(np.random.permutation(n), k)
    cv_rmse  = []

    for param in param_grid:
        fold_err = []
        for fi in fold_idx:
            mask     = np.ones(n, dtype=bool)
            mask[fi] = False
            try:
                _, xi_cv, beta_cv = fit_fn(y[mask], X[mask], param)
                yp  = cond_mean(xi_cv, beta_cv, X[fi])
                fin = np.isfinite(yp)
                if fin.sum() < 2:
                    fold_err.append(np.inf)
                    continue
                fold_err.append(
                    np.sqrt(np.mean((yp[fin] - y[fi][fin])**2)))
            except Exception:
                fold_err.append(np.inf)
        cv_rmse.append(np.nanmean(fold_err))

    best_idx   = int(np.argmin(cv_rmse))
    best_param = param_grid[best_idx]
    print(f"    Best param = {best_param:.5f}  "
          f"(CV-RMSE = {cv_rmse[best_idx]:.4f})")
    return best_param, np.array(cv_rmse)


def cv_lasso(y, X, lam_grid=None, k=5):
    if lam_grid is None:
        lam_grid = np.logspace(-2, 2, 50)
    print("  [Lasso] 5-fold CV for lambda ...")
    best, curve = _kfold_cv(y, X, fit_lasso, lam_grid, k)
    return best, lam_grid, curve


